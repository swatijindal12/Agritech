// Add a new Farm [only farmer ]
exports.addFarm = async (req, res, next) => {
 
  // 1. Handle File Upload .
  const presignedURL = await uploadImage(req.files.file);
 
  let ipfs_hash = "";

  //Create option to give name to ipfs storage.
  const options = {
    pinataMetadata: {
      name: req.user.id,
    },
    pinataOptions: {
      cidVersion: 0,
    },
  };

  // Saving data to ipfs
  try {
    const result = await pinata.pinJSONToIPFS(req.body,options);
    ipfs_hash = result["IpfsHash"];
  } catch (error) {
    return res.status(500).json({
      error: "internal server error,try again",
      message: null,
      httpStatus: 400,
      data: null,
    });
  }

  // web3 provider for contracts..
  const provider = new Provider(Private_Key, rpcURL);
  const web3 = new Web3(provider);
  const farmNFTContract = new web3.eth.Contract(FarmABI, AdminContractAddr);


   // Create a new farm [only farmer can create]
   try {
    //Create a farm in DB
    const farm = await Farm.create({
      ipfs_hash,
      farmnft_id: "dsjkiu34iu43hbdhuhu34hjKSSJ",
      user_id: req.user.id,
      image_url:presignedURL,
      validated_status: false
    });

    // Returning successful response.
    return res.status(201).json({
      error: null,
      message: "farm created.",
      httpStatus: 201,
      data: farm,
    });

  } catch (error) {
    return res.status(400).json({
      error: `operation failed, try again !`,
      message: null,
      httpStatus: 400,
      data: null,
    });
  }
  
};

